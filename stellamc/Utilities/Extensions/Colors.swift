import UIKit

extension UIColor {
    class var backGrey: UIColor {
        return UIColor(named: "BackGrey") ?? .clear
    }

    class var frontOrange: UIColor {
        return UIColor(named: "FrontOrange") ?? .clear
    }

    class var backLightGrey: UIColor {
        return UIColor(named: "BackLightGrey") ?? .clear
    }
}
